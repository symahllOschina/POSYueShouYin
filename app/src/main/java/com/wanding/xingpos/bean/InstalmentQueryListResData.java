package com.wanding.xingpos.bean;

import java.io.Serializable;
import java.util.List;

/**
 * 分期查询订单列表返回实体
 */
public class InstalmentQueryListResData implements Serializable {

    private String pageNum;//pageNum	是	N1..9999	当前页
    private String pageSize;//pageSize	是	N1..2	每页条数
    private String total;//total	是	N1..9999	总记录数
    private String pages;//pages	是	N1..9999	总页数

    private List<InstalmentQueryResData> REC;


    private String respCode;//"respCode": "000000",
    private String respMsg;//"respMsg": "成功"

    public InstalmentQueryListResData() {

    }

    public String getPageNum() {
        return pageNum;
    }

    public void setPageNum(String pageNum) {
        this.pageNum = pageNum;
    }

    public String getPageSize() {
        return pageSize;
    }

    public void setPageSize(String pageSize) {
        this.pageSize = pageSize;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public String getPages() {
        return pages;
    }

    public void setPages(String pages) {
        this.pages = pages;
    }

    public List<InstalmentQueryResData> getREC() {
        return REC;
    }

    public void setREC(List<InstalmentQueryResData> REC) {
        this.REC = REC;
    }

    public String getRespCode() {
        return respCode;
    }

    public void setRespCode(String respCode) {
        this.respCode = respCode;
    }

    public String getRespMsg() {
        return respMsg;
    }

    public void setRespMsg(String respMsg) {
        this.respMsg = respMsg;
    }
}
